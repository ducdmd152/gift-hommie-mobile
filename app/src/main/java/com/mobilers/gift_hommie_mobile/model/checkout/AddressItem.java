package com.mobilers.gift_hommie_mobile.model.checkout;

public class AddressItem {
    private int code;
    private String name;

    public AddressItem(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
